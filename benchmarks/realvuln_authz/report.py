#!/usr/bin/env python3
"""Render REPORT.md from scores.json. Honest framing baked in (independent study,
not a Semgrep reproduction, reject the conclusion if unsupported).

  python benchmarks/realvuln_authz/report.py --scores .../results/v1/scores.json \\
    --output benchmarks/realvuln_authz/REPORT.md
"""

import argparse
import json
from pathlib import Path

SYS_LABEL = {"frame_authz_symbolic": "Frame symbolic", "frame_glm52_authz": "Frame+GLM"}

INTRO = """\
# RealVuln Authorization / IDOR-BOLA Study

A commercial Semgrep study reported that combining deterministic program analysis
with LLM reasoning improved repository-scale detection of IDOR vulnerabilities. The
applications and complete evaluator used in that study were not publicly released.
We therefore conduct an independent experiment on a frozen public subset of RealVuln,
using human-authored Python applications with public file-, line- and CWE-level
ground truth. This study evaluates whether Frame's open neuro-symbolic architecture
can perform the same general class of authorization analysis. It is **not** a
reproduction of Semgrep's benchmark and its scores are **not** directly comparable to
Semgrep's published results.

**Systems evaluated** (both are direct `frame` invocations):

- `frame_authz_symbolic` — `frame scan`: sound taint-based IDOR candidates
  (attacker-controlled path/query/body identifier reaches a resource-selection sink).
- `frame_glm52_authz` — `frame scan --ai`: the same symbolic candidates, with GLM-5.2
  adjudicating ownership/authorization (the semantic judgment the taint engine cannot
  soundly make).
"""

LIMITATIONS = """\
## Limitations

- Semgrep's original application set is private; this is **not** a head-to-head vendor comparison.
- RealVuln applications are intentionally vulnerable training applications.
- The core suite has only **8 positive cases** and 2 safe controls; no strong statistical claims.
- The ground truth is public and therefore not a permanently blind test.
- VAmPI was used as a development smoke test; DVBLab / Vulpy / DVGA were held out for this cycle.
- All applications are Python; several cases are deliberately obvious.
- Authorization CWEs overlap (CWE-639 / 862 / 863 / 284 / 285); matching accepts a CWE family per case.
- Static detection does not prove exploitability; the LLM can be stochastic despite temperature 0.
- Unlabelled real vulnerabilities may exist; results do not generalize to production apps without more evaluation.
"""


def _table(headers, rows):
    out = ["| " + " | ".join(headers) + " |",
           "|" + "|".join("---" for _ in headers) + "|"]
    for r in rows:
        out.append("| " + " | ".join(str(c) for c in r) + " |")
    return "\n".join(out)


def render(scores: dict) -> str:
    systems = list(scores["systems"].keys())
    # union of all case ids, preserving per_case order from the first system
    any_sys = scores["systems"][systems[0]]
    case_ids = list(any_sys["per_case"].keys())

    # Table A: case-level
    a_rows = []
    for cid in case_ids:
        base = any_sys["per_case"][cid]
        cells = [cid, base["repository"], base["operation"]]
        for s in systems:
            pc = scores["systems"][s]["per_case"][cid]
            mark = "detected" if pc["majority"] else "-"
            if pc["n_runs"] > 1:
                mark += f" ({pc['detected_runs']}/{pc['n_runs']})"
            cells.append(mark)
        note = "SAFE control (want: not flagged)" if base["expected"] == "safe" else base["class"]
        cells.append(note)
        a_rows.append(cells)
    table_a = _table(["Case", "Repo", "Operation"] + [SYS_LABEL.get(s, s) for s in systems] + ["Notes"], a_rows)

    # Table B: aggregate
    b_rows = []
    for s in systems:
        m = scores["systems"][s]
        b_rows.append([SYS_LABEL.get(s, s), m["true_positives"], m["false_negatives"],
                       f"{m['safe_controls_passed']}/2", m["precision"], m["recall"], m["f1"], m["f2"]])
    table_b = _table(["System", "TP", "FN", "Safe passed", "Precision", "Recall", "F1", "F2"], b_rows)

    # honest headline (only the supported claim)
    fg = scores["systems"].get("frame_glm52_authz")
    headline = ""
    if fg:
        headline = (f"\nOn this public benchmark, Frame's symbolic endpoint and authorization "
                    f"analysis structured GLM-5.2's reasoning across Flask and GraphQL "
                    f"applications, detecting **{fg['true_positives']} of 8** labelled IDOR/BOLA "
                    f"cases while correctly suppressing **{fg['safe_controls_passed']} of 2** "
                    f"explicit authorization-safe controls.\n")

    return "\n".join([
        INTRO, headline,
        "## Case-level results\n", table_a, "",
        "## Aggregate quality\n", table_b, "",
        LIMITATIONS,
        "\n<sub>Generated by report.py from results/*/scores.json. Frame provenance: "
        "`symbolic` = sound taint reachability; `llm_verified` = GLM-adjudicated. "
        "Static detection is not proof of exploitability.</sub>\n",
    ])


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--scores", required=True)
    ap.add_argument("--output", default=str(Path(__file__).resolve().parent / "REPORT.md"))
    args = ap.parse_args()
    scores = json.loads(Path(args.scores).read_text())
    Path(args.output).write_text(render(scores))
    print(f"[report] wrote {args.output}")
    return 0


if __name__ == "__main__":
    import sys
    sys.exit(main())
